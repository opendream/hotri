<h1>เพิ่ม - แก้ไขรายการบรรณานุกรม:</h1>
สำหรับการเพิ่มและแก้ไขรายการบรรณานุกรมโดยใช้มาตรฐาน  <a href="javascript:popSecondaryLarge('http://www.loc.gov/marc')">MARC 21</a> เท่านั้น
การเพิ่มรายการบรรณานุกรมไปเพิ่มที่เมนเพิ่มรายการบรรณานุกรม
การแก้ไขรายการบรรณานุกรมไปทแก้ไขข้อมูลรายการบรรณานุกรม
<br><br>
ถ้าเพิ่มรายการบรรณานุกรมเรียบร้อยคุณจะต้องเพิ่มรายการตัวเล่มก่อนสำหรับการยืมออกต่อไป
<br><br>
แนะนำการใช้งาน:
